phrase="Hello World"
print(phrase[0])#prints the first index that is H of the string
print(phrase.upper())#prints the string in upper case
print(phrase.lower())#prints the string in lower case
print(phrase.isupper())#gives True if the whole string is in upper case False otherwise
print(len(phrase))#gives the length of the string
print(phrase.index("Wor"))#gives the starting index of a single character or a phrase eg Wor starts at index 6
print(phrase.replace("World","Earth"))#replaces the first parameter with the second in the specified string
print(phrase.replace("l","L"))#replaces all the letters in the string
