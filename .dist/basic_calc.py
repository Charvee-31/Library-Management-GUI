num1=int(input("Enter the first number:"))
num2=int(input("Enter the second number:"))
op=input("Enter the operation(+,-,*,/):")
if op=="+":
    print(str(num1)+"+"+str(num2)+"="+str(num1+num2))
elif op== "-":
    print(str(num1)+"-"+str(num2)+"="+str(num1-num2))
elif op=="*":
    print(str(num1)+"X"+str(num2)+"="+str(num1*num2))
elif op=="/":
    print(str(num1)+"/"+str(num2)+"="+str(num1/num2))
else:
    print("ERROR!!")
    


    

